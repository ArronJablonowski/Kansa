Get-ChildItem -Path ".\..\..\" -Filter "*ProcImgPath.csv" -Recurse | ?{$_.PSIsContainer -eq $false -and $_.length -lt 300} | ?{Remove-Item $_.fullname}

if (Get-Command logparser.exe) {

    $lpquery = @"
    SELECT
        COUNT([ProcessName], 
        [FileHash]) as ct,
        [ProcessName], 
        [FileHash]
    FROM
        *ProcImgPath.csv
    GROUP BY
        [ProcessName],
        [FileHash]    
    ORDER BY
        ct desc
"@

    & logparser -stats:off -i:csv -dtlines:0 -o:csv $lpquery

} else {
