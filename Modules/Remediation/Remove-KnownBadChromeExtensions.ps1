    $folder = $($user.FullName)+"\AppData\Local\Google\Chrome\User Data\Default\Extensions"
    If(Test-Path $folder){
        $extensionFolders = Get-ChildItem $folder
        foreach($extension in $extensionFolders) {
            $fullExtPath = "$folder\$extension" # Get the full path the pass to checkExtensionName, for possible deletion
